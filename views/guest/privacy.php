<h3>Chính sách sử dụng dịch vụ tạo album tại album.ocdao.net</h3>

<p><strong>1. Các thay đổi với album.ocdao.net</strong></p>
<p>Chúng tôi, với toàn quyền hạn của mình và vào bất cứ lúc nào được quyền bổ sung, sửa đổi hay xóa bỏ bất kỳ thông tin nào cũng như thay đổi giao diện, sự trình bày, thành phần hoặc chức năng, nội dung của trang web này bao gồm bất kỳ khoản mục nào mà không cần báo trước.</p> 

<p><strong>2. Thay đổi quy định</strong></p>
<p>Chúng tôi có toàn quyền thay đổi quy định mà không cần báo trước. Với việc tiếp tục sử dụng album.Ocdao.net sau những sửa đổi đó, bạn mặc nhiên đồng ý chấp hành các sửa đổi trong quy định.</p>

<p><strong>3. Bản quyền</strong></p>
<p>Chúng tôi không chịu trách nhiệm tất cả các vấn đề bản quyền liên quan tới hình ảnh và âm nhạc mà người dùng sử dụng để tạo album.Bạn phải chịu trách nhiệm bồi thường thiệt hại dân sự, bị xử phạt vi phạm hành chính, bị truy tố trách nhiệm hình sự nếu bạn có hành vi vi phạm quyền tác giả quyền liên quan.</p>

<p><strong>4. Cung cấp nội dung cho album.ocdao</strong></p>
<p>Bạn đồng ý trao cho chúng tôi toàn bộ quyền và cấp phép (bao gồm cả các quyền về tinh thần hay các quyền cần thiết khác) để sử dụng, trưng bày, tái sản xuất, chỉnh sửa, làm cho phù hợp, xuất bản, cung cấp, xúc tiến, dịch và tạo ra các phiên bản phái sinh hoặc tổ hợp khác, một phần hoặc toàn bộ, trên phạm vi toàn cầu mà không đòi hỏi thù lao. Sự cấp phép này được áp dụng với bất cứ dạng thức, phương tiện, công nghệ nào đã được biết đến hoặc phát triển sau này; bạn cần lưu ý rằng, các bài viết (post) trên diễn đàn, hoặc tại các khu vực được phép có thể tiếp tục nằm trên website của chúng tôi ngay cả khi tài khoản của bạn đã bị xóa vì bất kỳ lý do gì.</p> 

